/**
 * Muestra los números múltiplos de 5 de 0 a 100
 * utilizando un bucle while.
 *
 * Gonzalo Iván Chaparro Barese
 * Capítulo 5 - Ejercicio 2
 */
  
public class Ejercicio02 {

  public static void main(String[] args) {
    
    int i = 0;
    
    while(i <= 100) {
      System.out.println(i);
      i+=5;
    }
  }
}
