public class Ejercicio01 {
  public static void main(String[] args) {
    int x;
    int y;
    x = 144;
    y = 999;

    System.out.println("El valor de la variable x es: " + x);
    System.out.println("El valor de la variable y es: " + y);
    System.out.println("La suma de ambas variables da como resultado: " + (x + y));
    System.out.println("La resta de la variable y menos la variable x es: " + (y - x));
    System.out.println("Si dividimos la variable y entre la variable x nos da como resultado: " + (y / x));
    System.out.println("El producto de multiplicar las variables x e y es: " + (x * y));
  }
}
